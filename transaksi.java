    private void btTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btTambahActionPerformed
      if("".equals(txtid.getText()) || "".equals(txtcustomer.getText()) || "".equals(txtnama.getText()) || "".equals(txtalamat.getText()) || "".equals(txtharga.getText()) || "".equals(txtquantity.getText()) || "".equals(txttotal.getText()) || "".equals(txtpembeli.getText()) || "".equals(txtkembalian.getText())) {
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data","Error",JOptionPane.WARNING_MESSAGE);    
        }else{
            String Jenis = "";
            if (rdCreamy.isSelected()){
                    Jenis = "Creamy";
                }else{
                    Jenis = "Juicy";
                }
                String SQL = "INSERT INTO transaksi (id_barang,nama_customer,nama_liquid,alamat,harga,jenis_liquid,quantity,total_harga,uang_pembeli,kembalian) VALUES ('"+Integer.valueOf(txtid.getText())+"','"+txtcustomer.getText()+"','"+txtnama.getText()+"','"+txtalamat.getText()+"','"+Integer.valueOf(txtharga.getText())+"','"+Jenis+"','"+Integer.valueOf(txtquantity.getText())+"','"+Integer.valueOf(txttotal.getText())+"','"+Integer.valueOf(txtpembeli.getText())+"','"+Integer.valueOf(txtkembalian.getText())+"')";
                int status = koneksi.execute(SQL);
                if(status == 1){
                    JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan","Sukses", JOptionPane.INFORMATION_MESSAGE);
                    selectData();
                }else{
                   JOptionPane.showMessageDialog(this, "Data gagal ditambahkan","Sukses", JOptionPane.INFORMATION_MESSAGE); 
                }
            } 
        
        
    }//GEN-LAST:event_btTambahActionPerformed

    private void bthapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bthapusActionPerformed
        // TODO add your handling code here:
        int baris = tblData.getSelectedRow();
        if(baris != -1){
            String id_liquid = tblData.getValueAt(baris, 0).toString();
            String SQL = "DELETE FROM transaksi WHERE id_barang='"+id_liquid+"'";
            int status = koneksi.execute(SQL);
            if (status==1){
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus","Sukses", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(this, "Data gagal dihapus","Sukses", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_bthapusActionPerformed

