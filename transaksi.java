    private void tblDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDataMouseClicked
        // TODO add your handling code here:
       int baris = tblData.getSelectedRow();
        if(baris != -1){
            txtid.setText(tblData.getValueAt(baris,0).toString());
            txtcustomer.setText(tblData.getValueAt(baris,1).toString());
            txtnama.setText(tblData.getValueAt(baris,2).toString());
            txtalamat.setText(tblData.getValueAt(baris,3).toString());
            txtharga.setText(tblData.getValueAt(baris,4).toString());
            if("Creamy".equals(tblData.getValueAt(baris,5).toString())){
                rdCreamy.setSelected(true);
            }else{
                rdJuicy.setSelected(true);
            }
            txtquantity.setText(tblData.getValueAt(baris,6).toString());
            txttotal.setText(tblData.getValueAt(baris,7).toString());
            txtpembeli.setText(tblData.getValueAt(baris,8).toString());
            txtkembalian.setText(tblData.getValueAt(baris,9).toString());
        }
    }//GEN-LAST:event_tblDataMouseClicked

